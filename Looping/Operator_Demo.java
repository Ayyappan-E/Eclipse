package Looping;

public class Operator_Demo {

	public static void main(String[] args) {
		

				int no1 = 1, no2 = 2, no3 = 3; 
				  System.out.println(no1++ + no2++ + no3++);
				    
				  no1 = 1;
				  no2 = 2;
				  no3 = 3; 
				  System.out.println(no1++ - no2++ - no3++);
				  
				  no1 = 1;
				  no2 = 2;
				  no3 = 3; 
				  System.out.println(++no1 + ++no2 + ++no3);
				  
				  no1 = 1;
				  no2 = 2;
				  no3 = 3; 
				  System.out.println(++no1 - ++no2 - ++no3);

			      no1 = 1 ;
				  no2 = 2;
				  no3 = 3; 
				  System.out.println(no1++ + ++no1 + no1-- + --no1);
				  
				  no1 = 1 ;
				  no2 = 2;
				  no3 = 3; 
				  System.out.println(-no1++);
				  

	}

}
